       <h1> Welcome to Python! </h1>
<p> Why to Learn Python? </p>
<p> Python is a high-level, interpreted, interactive and object-oriented scripting language. Python is designed to be highly readable. It uses English keywords frequently where as other languages use punctuation, and it has fewer syntactical constructions than other languages.
Python is a MUST for students and working professionals to become a great Software Engineer specially when they are working in Web Development Domain. I will list down some of the key advantages of learning Python:
<p/>
<ul> 
    <li>Python is Interpreted − Python is processed at runtime by the interpreter. You do not need to compile your program before executing it.This is similar to PERL and PHP.</li> 
    <li>Python is Interactive − You can actually sit at a Python prompt and interact with the interpreter directly to write your programs.</li>
    <li>Python is Object-Oriented − Python supports Object-Oriented style or technique of programming that encapsulates code within objects.
    <li>Python is a Beginner's Language − Python is a great language for the beginner-level programmers and supports the development of a wide range of applications from simple text processing to WWW browsers to games.
</li>
</ul>
    <h1>Characteristics of Python</h1>
    <p>Following are important characteristics of Python Programming −.</p>
    <ul>
    <li>It supports functional and structured programming methods as well as OOP.</li>
    <li>It can be used as a scripting language or can be compiled to byte-code for building large applications.</li>
    <li>It provides very high-level dynamic data types and supports dynamic type checking.</li>
    <li>It supports automatic garbage collection.</li>
    <li>It can be easily integrated with C, C++, COM, ActiveX, CORBA, and Java.</li>
    </ul>


<h2>Applications of Python</h2>
<p>As mentioned before, Python is one of the most widely used language over the web. I'm going to list few of them here:</p>
<ul>
    <li>Easy-to-learn − Python has few keywords, simple structure, and a clearly defined syntax. This allows the student to pick up the language quickly.</li>
    <li>Easy-to-read − Python code is more clearly defined and visible to the eyes.</li>
    <li>Easy-to-maintain − Python's source code is fairly easy-to-maintain.</li>
    <li>A broad standard library − Python's bulk of the library is very portable and cross-platform compatible on UNIX, Windows, and Macintosh.</li>
    <li>Interactive Mode − Python has support for an interactive mode which allows interactive testing and debugging of snippets of code.</li>
    <li>Portable-Python can run on a wide variety of hardware platforms and has the same interface on all platforms. </li>
    <li>Extendable−You can add low-level modules to the Python interpreter. These modules enable programmers to add to or customize their tools to be more efficient.</li>
    <li>Databases − Python provides interfaces to all major commercial databases.</li>
    <li>GUI Programming − Python supports GUI applications that can be created and ported to many system calls, libraries and windows systems, such as Windows MFC, Macintosh, and the X Window system of Unix.</li>
    <li>Scalable − Python provides a better structure and support for large programs than shell scripting.</li>
</ul>

<h3>History of Python</h3>
<p>Python was developed by Guido van Rossum in the late eighties and early nineties at the National Research Institute for Mathematics and Computer Science in the Netherlands.
Python is derived from many other languages, including ABC, Modula-3, C, C++, Algol-68, SmallTalk, and Unix shell and other scripting languages.
Python is copyrighted. Like Perl, Python source code is now available under the GNU General Public License (GPL).
Python is now maintained by a core development team at the institute, although Guido van Rossum still holds a vital role in directing its progress.</p>

